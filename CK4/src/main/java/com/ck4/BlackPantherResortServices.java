    private void printButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_printButtonActionPerformed

        try {
            if (ticketIDField.getText().isBlank()) {
                JOptionPane.showMessageDialog(this, "Ticket ID cannot be blanked.");
                ticketIDField.requestFocus();
                return;
            }
            if (customerNameField.getText().isBlank()) {
                JOptionPane.showMessageDialog(this, "Customer Name cannot be blanked.");
                customerNameField.requestFocus();
                return;
            }
            if (customerIDField.getText().isBlank()) {
                JOptionPane.showMessageDialog(this, "Customer ID cannot be blanked.");
                customerIDField.requestFocus();
                return;
            }
            if (customerPhoneField.getText().isBlank()) {
                JOptionPane.showMessageDialog(this, "Customer Phone cannot be blanked.");
                customerPhoneField.requestFocus();
                return;
            }
            if (noOfDayField.getText().isBlank()) {
                JOptionPane.showMessageDialog(this, "No Of Day cannot blanked.");
                noOfDayField.requestFocus();
                return;
            }
            if (voucherField.getText().isBlank()) {
                JOptionPane.showMessageDialog(this, "Voucher cannot be blanked.");
                voucherField.requestFocus();
                return;
            }

            int ticketID = Integer.parseInt(ticketIDField.getText());
            String customerName = customerNameField.getText();
            String customerID = customerIDField.getText();
            String customerPhone = customerPhoneField.getText();
            int noOfDay = Integer.parseInt(noOfDayField.getText());
            String bungalowType = (String) bungalowTypeComboBox.getSelectedItem();
            String extraPackage = "";
            if (buffetCheckBox.isSelected()) {
                extraPackage += "Buffet, ";
            }
            if (spaCheckBox.isSelected()) {
                extraPackage += "Spa, ";
            }
            if (gymCheckBox.isSelected()) {
                extraPackage += "Gym, ";
            }
            extraPackage = extraPackage.isEmpty() ? "None" : extraPackage.substring(0, extraPackage.length() - 2);
            float voucher = Float.parseFloat(voucherField.getText());

            BungalowRentalTicket ticket = new BungalowRentalTicket(ticketID, customerName, customerID, customerPhone, bungalowType, noOfDay, extraPackage, voucher);
            float payment = ticket.getPayment();

            listTicket.add(ticket);

            printArea.setText(ticket.toString());

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ticket ID, No Of Day and Voucher should be numbers.");
            return;
        }
        fillInTicketTable();
    }//GEN-LAST:event_printButtonActionPerformed

