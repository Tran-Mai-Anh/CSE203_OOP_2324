    public void updateDateFromTable() {
        DefaultTableModel model = (DefaultTableModel) ticketTable.getModel();

        int selectedRow = ticketTable.getSelectedRow();
        BungalowRentalTicket ticket = listTicket.get(selectedRow);
        customerNameField.setText(ticket.getCustomerName().toString());
        customerIDField.setText(ticket.getCustomerID().toString());
        customerPhoneField.setText(ticket.getCustomerPhone().toString());
        noOfDayField.setText(String.valueOf(ticket.getNoOfDay()));
        bungalowTypeComboBox.setSelectedItem(ticket.getBungalowType());

        listTicket.set(selectedRow, ticket);
    }

    public void fillInTicketTable() {
        DefaultTableModel model = (DefaultTableModel) ticketTable.getModel();

        int rowCount = model.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--) {
            model.removeRow(i);
        }

        for (BungalowRentalTicket ticket : listTicket) {
            Object[] rows = new Object[]{ticket.getTicketID(), ticket.getCustomerName(), ticket.getCustomerID(), ticket.getCustomerPhone(), ticket.getNoOfDay(), ticket.getBungalowType(), ticket.getExtraPackage()};
            model.addRow(rows);
        }

        pack();
    }
